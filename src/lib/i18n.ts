export function t(key: string, lang: Sprache, replacements?: Record<string, string | number>): string {
  let result: string;

  if (lang === "de") {
    result = de[key] ?? key;
  } else if (lang === "de-en") {
    const d = de[key] ?? key;
    const e = en[key] ?? key;
    result = d === e ? d : `${d} / ${e}`;
  } else if (lang === "de-fr") {
    const d = de[key] ?? key;
    const f = fr[key] ?? key;
    result = d === f ? d : `${d} / ${f}`;
  } else {
    result = de[key] ?? key;
  }

  if (replacements) {
    Object.entries(replacements).forEach(([k, v]) => {
      result = result.replaceAll(`{${k}}`, String(v));
    });
  }

  return result;
}
